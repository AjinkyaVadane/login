package com.example.vsreenathreddy1.login;

public class LostFound {
    String name;
    String description;
    String location;
    String date_time;
    String id;

    public LostFound(String name, String description, String location, String date_time, String id) {
        this.name = name;
        this.description = description;
        this.location = location;
        this.date_time = date_time;
        this.id = id;
    }
}
